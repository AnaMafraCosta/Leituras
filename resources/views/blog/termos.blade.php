        <h1 class="h3 mb-0" style="color: #D92B04;">Termos Literários</h1>
    </div>
            <ul>
            <li><b>BOOK HAUL:</b>
            O Book Haul é, nada mais, nada menos do que o famoso “Recebidos”. É quando as pessoas fazem vídeos mostrando os livros adquiridos naquele mês.
            </li><br>
            <li><b>HYPE: </b>
            Essa é uma palavra que serve para várias categorias do entretenimento, além da literatura. Hype é o livro que está na moda, que está bombando no momento e que todo mundo está lendo ou quer ler. Ainda existe uma variação do “Hype”, que é a palavra “Hypado”. Ambas significam a mesma coisa.
            </li><br>
            <li><b>PLOT TWIST: </b>
            Essa é uma expressão utilizada quando acontece algo na história que é bastante inesperado. Normalmente isso acontece no meio ou no final do livro. Então, os leitores ficam perdidos e isso confunde o cérebro da gente haha
            </li><br>
            <li><b>BOOKSHELF TOUR: </b>
            Aqui temos o sentido literal da palavra. Significa “Tour pela estante”, ou seja, as pessoas vão fazer um passeio pela estante mostrando todos os livros que tem nela e a forma como arruma.
            </li><br>
            <li><b>OTP – ONE TRUE PAIRING: </b>
            OTP é muito usado quando existe uma combinação perfeita entre dois personagens na história. Quando eles formam um casal único.
            </li><br>
            <li><b>HARDCOVER: </b>
            Essa é uma expressão utilizada para representar um livro de capa dura.
            </li><br>
            <li><b>BOOKTUBER/ BOOKSGRAM: </b>
            Esses dois termos são usados para produtores de conteúdo relacionado à livros como, vídeos, resenhas, indicações no YouTube e no Instagram.
            </li><br>
            <li><b>TBR – TO BE READ: </b>
            Essa é uma das expressões mais usadas no meio literário. Ela significa “Para ser lido” e, normalmente, são listas de leituras que as pessoas pretendem fazer em breve. Basicamente, são os próximos livros a serem lidos.
            </li><br>
            <li><b>SPRINT: </b>
            Sprint significa uma alta velocidade possível ser atingida em dado momento. Quando relacionamos isso com os livros, chamamos de “sprint de leitura”. Esse termo é usado quando o leitor decide tirar um certo tempo APENAS para ler, sem nenhuma distração.
            </li><br>
            <li><b>FANFIC: </b>
            Essa é uma palavra utilizada para histórias criadas por fãs de alguns livros. Eles usam os mesmos personagens, porém mudam o enredo, fazem um final diferente, entre outras coisas.
            </li><br>
            <li><b>Ressaca Literária: </b>
            É um período em que o leitor não sente ânimo e nem vontade de ler. Normalmente, acontece após a leitura de um livro que mexeu muito com você. Pode ser de forma positiva ou negativa, após ler uma história muito boa ou que não conversou tanto.
            </li><br>
            <li><b>Bookstan: </b>
            Somos nós, leitores! Todas as pessoas que gostam de ler ou são apaixonadas pelo universo da leitura podem ser consideradas bookstans.
            </li><br>
            <li><b>Paperback: </b>
            É o típico livro de brochura com capa mole. Ou seja, o modelo que mais utilizamos aqui no Brasil.
            </li><br>
            <li><b>Crush Literário: </b>
            É um termo usado para designar o personagem por quem você se apaixonou naquela história.
            </li><br>
            <li><b>Jacket: </b>
            É uma folha de guarda que envolve a capa do livro. Em alguns casos, principalmente nos livros de capa dura, todas as informações e ilustrações do exemplar vem na jacket.
            </li><br>
            <li><b>Wishlist: </b>
            É a sua lista de livros desejados. Seja um link na Amazon, uma lista no celular ou aqueles títulos anotados em uma folha de papel. Todas são wishlists!
            </li><br>
            <li><b>Gatilho: </b>
            Apesar desse termo servir para vários outros nichos, ele também é muito presente na literatura. Nos livros o gatilho é considerado uma situação que pode provocar uma reação nos leitores. Essas reações podem ser positivas ou negativas, por isso é preciso ter muito cuidado e sempre alertar a presença de situações assim.
            </li><br>
            <li><b>Clifhanger: </b>
            Uma revelação muito surpreendente que aparece no final do livro e acaba gerando um gancho para sequência.
            </li><br>
            <li><b>Best-seller: </b>
            São aqueles livros sucesso de venda. Ou seja, traduzindo para o português, o termo significa “bem-sucedido” e é considerado muito popular entre os leitores.
            </li><br>
            <li><b>Quotes: </b>
            São citações que o leitor considera relevante ou impactante. Sendo assim, ele vai lá e faz uma marcação dessa frase.
            </li>
        </ul>
